package classpart09;

public class ClassEx09 {
	
	// 디폴트 생성자
	public ClassEx09() {};
	
	// 더하기
	public int plus(int x, int y) {
		return x+y;
	}
	// 빼기
	public int minus(int x, int y) {
		return x-y;
	}
	// 곱하기
	public int gop(int x, int y) {
		return x*y;
	}
	// 나누기
	public int na(int x, int y) {
		return x/y;
	}
}
